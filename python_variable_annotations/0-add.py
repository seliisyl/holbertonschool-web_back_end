#!/usr/bin/env python3
"""
This module provides a function to add two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers and return their sum.

    Parameters:
    a (float): The first float number to be added.
    b (float): The second float number to be added.

    Returns:
    float: The sum of the two input float numbers.

    Example:
    >>> add(1.5, 2.3)
    3.8
    """
    return a + b
