#!/usr/bin/env python3
"""
This module provides a function to get the string representation
of a floating point number.
"""


def to_str(n: float) -> str:
    """
    Convertit un nombre flottant en une chaîne de caractères.

    Paramètre :
    n (float) : Le nombre flottant à convertir en chaîne de caractères.

    Retourne :
    str : La représentation en chaîne de caractères du nombre flottant.

    Exemple :
    >>> to_str(3.14)
    '3.14'
    """
    return str(n)
